/*
* Declaração classica de função
function soma(x, y) {
    const resultado = x + y;
    return resultado;
}
const resultado = soma(4, 6);
console.log(resultado);
*/

/*
* Funcão anônima
const raiz = function (n) {
    return n ** 0.5;
};
console.log(raiz(9));
*/

/*
* Arrow Function
const raiz = n => return n ** 0.5;
console.log(raiz(9));
*/